/*
  * Valores numericos = (int, double, float, long).
  * Texto = (String) com a primeira letra maiuscula.
  * Booleanos/ = boolean (True e False)

*/


public class TiposDados {
  public static void main(String[] args) {
    
    // Criando uma variavel do tipo int
    int dadoDoTipoInt = 10;

    // Criando uma variavel do tipo double
    double dadoDoTipoDouble = 2.46;

    // Criando uma variavel do tipo float
    float dadoDoTipoFloat = 3.14f; //precisa add a letra"f" no final.

    // Criando uma variavel do tipo long
    long dadoDoTipoLong = 1234242542L; //precisa add a letra "L" no final.

    // Criando uma variavel do tipo String
    String dadoDoTipoString = "Henrique";

    // Criando uma variavel do tipo boolean
    boolean dadoDoTipoBoolean = true;

  }
}
