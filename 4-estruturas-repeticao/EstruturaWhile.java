

public class EstruturaWhile {
  public static void main(String[] args) {
    int valorInicial = 0;

    while (valorInicial < 5) {
      System.out.println("O valor é: " + valorInicial);

      // add +1 ao valor que está armazenado.
      valorInicial++;
      
    }

    System.out.println("While finalizado!");
  }
}
